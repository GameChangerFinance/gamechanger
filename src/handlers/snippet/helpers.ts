export const toJSLiteral = (value?: string) =>
  value === undefined ? 'undefined' : JSON.stringify(value)

export const toBooleanLiteral = (value?: boolean) => (value ? 'true' : 'false')

export const resolveSnippetOrigin = (
  apiVersion?: APIVersion,
  network?: NetworkType
) => GCDomains[apiVersion || DefaultAPIVersion][network || DefaultNetwork]

const ensureTrailingSlash = (value?: string) =>
  value ? (value.endsWith('/') ? value : `${value}/`) : value

/**
 * Applies snippetArgs overrides to an existing placeholder map.
 *
 * This enables complete control over every placeholder defined in snippetTokens.
 */
export const applySnippetArgs = (
  replacements: Record<string, string>,
  snippetArgs?: SnippetArgs
): Record<string, string> => {
  if (!snippetArgs) return replacements
  const next: Record<string, string> = { ...replacements }

  // Highest priority override for the whole defaultIntents map.
  if (typeof snippetArgs.defaultIntents === 'string') {
    next[snippetTokens.defaultIntentsBlock] = snippetArgs.defaultIntents
  }

  for (const [key, value] of Object.entries(snippetArgs)) {
    if (value === undefined || value === null) continue
    if (key === 'defaultIntents') continue
    // Recommended: override by snippetTokens key.
    if ((snippetTokens as any)[key]) {
      next[(snippetTokens as any)[key]] = String(value)
      continue
    }
    // Advanced: override by raw placeholder.
    next[key] = String(value)
  }
  return next
}

