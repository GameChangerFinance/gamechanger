export type SnippetHandlerArgs = {
  apiVersion: APIVersion
  network: NetworkType
  encoding: APIEncoding
  input: string
  debug?: boolean
  refAddress?: string
  disableNetworkRouter?: boolean
  /**
   * Optional wallet URL pattern override.
   * When provided, snippet outputs can consistently target a custom wallet base
   * (e.g. local development wallets) while preserving handler response types.
   */
  urlPattern?: string

  /**
   * Optional per-token overrides for snippet templates.
   *
   * Keys can be either:
   * - a key of `snippetTokens` (recommended), e.g. `title`, `twitterUrl`
   * - the raw placeholder string (advanced), e.g. `$#___TITLE___#$`
   *
   * Values are injected as-is (no escaping). For JS-literal tokens
   * (like apiVersion/network/encoding) pass a valid JS literal, e.g. `"2"`.
   */
  snippetArgs?: SnippetArgs
  qrResultType?: 'png' | 'svg'
  outputFile?: string
  template?: QRTemplateType | string
  styles?: string
}

export type SnippetArgs = Partial<
  Record<keyof typeof snippetTokens, string>
> & {
  /**
   * Raw JS object literal used to override the entire `defaultIntents` map.
   * This has higher priority than the default user intent injection.
   *
   * Useful when you want to add comments or additional intent definitions.
   */
  defaultIntents?: string
}

